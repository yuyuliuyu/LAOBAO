package com.lingnet.qxgl.entity;
 
import javax.persistence.Entity;
import javax.persistence.Table;

import com.lingnet.common.entity.BaseEntity;


/** 
 * @ClassName: QxDictionary 
 * @Description:   
 * @author 张明峰
 * @date 2013-7-30 下午5:30:16 
 *  
 */
@Entity
@Table(name = "QX_BK_DICTIONARY")
public class QxDictionary extends BaseEntity implements java.io.Serializable {

    private static final long serialVersionUID = 7083178687525589767L;
    
    private String name;
    private String flg;
    private String type;
    private Boolean isSystem;
    
    /**
     * @return the name
     */
    public String getName() {
        return name;
    }
    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }
    /**
     * @return the flg
     */
    public String getFlg() {
        return flg;
    }
    /**
     * @param flg the flg to set
     */
    public void setFlg(String flg) {
        this.flg = flg;
    }
    /**
     * @return the type
     */
    public String getType() {
        return type;
    }
    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }
    /**
     * @return the isSystem
     */
    public Boolean getIsSystem() {
        return isSystem;
    }
    /**
     * @param isSystem the isSystem to set
     */
    public void setIsSystem(Boolean isSystem) {
        this.isSystem = isSystem;
    }
   
}
