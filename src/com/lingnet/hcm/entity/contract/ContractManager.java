package com.lingnet.hcm.entity.contract;

import java.io.Serializable;
import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

import com.lingnet.common.entity.BaseEntity;

@Entity
@Table(name = "RS_CONTRACT_MANAGER")
public class ContractManager extends BaseEntity implements Serializable {
    private static final long serialVersionUID = 1L;
    private String userid;                  //员工id
    private String departid;                  //员工部门
    private String contractid;                //合同编号
    private Date contrstart;               //合同开始时间
    private Date contractend;               //合同结束日期 
    private String contracttype;               //合同类型
    private String contracttermtype;               //合同期限
    private String contractterm;               //合同期限类型
    private Date trialstart;               //试用期开始时间
    private Date trialend;               //试用期结束时间
    private String trialterm;               //试用期限
    private String postid;               //岗位id
    private String workaddr;               //工作地址
    private String worksys;               //工作制
    private String contractfirst;               //合同甲方
    private Date contract;               //合同签订日期
    private String contracttime;               //合同签订次数
    private String contractstate;               //合同状态
    private String renewstate;               //合同续签状态
    private String getbackup;               //合同领取状态
    private String relievereson;          //合同解除原因
    private String stopreson;          //合同中止原因
    private String remark;               //备注
    private String filed1;               //备用字段1
    private String filed2;               //备用字段2
    public ContractManager() {
        super(); 
    }
    @Column(name = "USERID")
    public String getUserid() {
        return userid;
    }
    public void setUserid(String userid) {
        this.userid = userid;
    }
    @Column(name = "CONTRACT_ID")
    public String getContractid() {
        return contractid;
    }
    public void setContractid(String contractid) {
        this.contractid = contractid;
    }
    @Column(name = "CONTR_START")
    public Date getContrstart() {
        return contrstart;
    }
    public void setContrstart(Date contrstart) {
        this.contrstart = contrstart;
    }
    @Column(name = "CONTR_END")
    public Date getContractend() {
        return contractend;
    }
    public void setContractend(Date contractend) {
        this.contractend = contractend;
    }
    @Column(name = "CONTR_TERM")
    public String getContractterm() {
        return contractterm;
    }
    public void setContractterm(String contractterm) {
        this.contractterm = contractterm;
    }
    @Column(name = "TRIAL_START")
    public Date getTrialstart() {
        return trialstart;
    }
    public void setTrialstart(Date trialstart) {
        this.trialstart = trialstart;
    }
    @Column(name = "TRIAL_END")
    public Date getTrialend() {
        return trialend;
    }
    public void setTrialend(Date trialend) {
        this.trialend = trialend;
    }
    @Column(name = "TRIAL_TERM")
    public String getTrialterm() {
        return trialterm;
    }
    public void setTrialterm(String trialterm) {
        this.trialterm = trialterm;
    }
    @Column(name = "POSTID")
    public String getPostid() {
        return postid;
    }
    public void setPostid(String postid) {
        this.postid = postid;
    }
    @Column(name = "WORK_ADDR")
    public String getWorkaddr() {
        return workaddr;
    }
    public void setWorkaddr(String workaddr) {
        this.workaddr = workaddr;
    }
    @Column(name = "WORK_SYS")
    public String getWorksys() {
        return worksys;
    }
    public void setWorksys(String worksys) {
        this.worksys = worksys;
    }
    @Column(name = "CONTRACT_FIRST")
    public String getContractfirst(){
        return contractfirst;
    }
    public void setContractfirst(String contractfirst) {
        this.contractfirst = contractfirst;
    }
    @Column(name = "CONTRACT_")
    public Date getContract() {
        return contract;
    }
    public void setContract(Date contract) {
        this.contract = contract;
    }
    @Column(name = "CONTRACT_STATE")
    public String getContractstate() {
        return contractstate;
    }
    public void setContractstate(String contractstate) {
        this.contractstate = contractstate;
    }
    @Column(name = "RENEW_STATE")
    public String getRenewstate(){
        return renewstate;
    }
    public void setRenewstate(String renewstate) {
        this.renewstate = renewstate;
    }
    @Column(name = "GET_BACKUP")
    public String getGetbackup() {
        return getbackup;
    }
    public void setGetbackup(String getbackup) {
        this.getbackup = getbackup;
    }
    @Column(name = "REMARK")
    public String getRemark() {
        return remark;
    }
    public void setRemark(String remark) {
        this.remark = remark;
    }
    @Column(name = "FILED1")
    public String getFiled1() {
        return filed1;
    }
    public void setFiled1(String filed1) {
        this.filed1 = filed1;
    }
    @Column(name = "FILED2")
    public String getFiled2() {
        return filed2;
    }
    public void setFiled2(String filed2) {
        this.filed2 = filed2;
    }
    @Column(name = "CONTRACT_TI")
    public String getContracttime() {
        return contracttime;
    }
    public void setContracttime(String contracttime) {
        this.contracttime = contracttime;
    }
    @Column(name = "CONTRACT_TYPE")
    public String getContracttype() {
        return contracttype;
    }
    public void setContracttype(String contracttype) {
        this.contracttype = contracttype;
    }
    @Column(name = "CONTRACT_TERM_TYPE")
    public String getContracttermtype() {
        return contracttermtype;
    }
    public void setContracttermtype(String contracttermtype) {
        this.contracttermtype = contracttermtype;
    }
    @Column(name = "DEPARTID")
    public String getDepartid() {
        return departid;
    }
    public void setDepartid(String departid) {
        this.departid = departid;
    }
    @Column(name = "RELIEVERESON")
    public String getRelievereson() {
        return relievereson;
    }
    public void setRelievereson(String relievereson) {
        this.relievereson = relievereson;
    }
    @Column(name = "STOPRESON")
    public String getStopreson() {
        return stopreson;
    }
    public void setStopreson(String stopreson) {
        this.stopreson = stopreson;
    }
}
